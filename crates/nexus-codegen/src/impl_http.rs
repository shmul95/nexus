#[cfg(test)]
mod tests {
    use nexus_core::{Contract, Schema, SchemaIdx, StructDef, Transport};

    use super::*;

    fn test_contract() -> (Contract, Schema) {
        let contract = Contract {
            name: "game_info".to_string(),
            transport: Transport::Http,
            schema: SchemaIdx(0),
        };
        let schema = Schema {
            name: "game_info".to_string(),
            structs: vec![StructDef {
                name: "game_info".to_string(),
                fields: vec![],
            }],
        };
        (contract, schema)
    }

    #[test]
    fn test_impl_generation() {
        let (contract, schema) = test_contract();
        let file = generate_impl(&contract, &schema).unwrap();

        assert_eq!(file.path, "src/nexus_game_info.c");

        let c = &file.content;

        // Includes
        assert!(
            c.contains("#include \"nexus_game_info.h\""),
            "missing own header include"
        );
        assert!(
            c.contains("#include <curl/curl.h>"),
            "missing curl include"
        );
        assert!(c.contains("#include <stdlib.h>"), "missing stdlib include");
        assert!(c.contains("#include <string.h>"), "missing string include");
        assert!(c.contains("#include <stdio.h>"), "missing stdio include");

        // Static variables
        assert!(
            c.contains("static CURL* _nexus_game_info_curl = NULL;"),
            "missing static curl handle"
        );
        assert!(
            c.contains("static char _nexus_game_info_url[512];"),
            "missing static url buffer"
        );

        // Write callback
        assert!(
            c.contains("_nexus_game_info_write_cb"),
            "missing write callback"
        );

        // Functions
        assert!(c.contains("nexus_game_info_init"), "missing init function");
        assert!(
            c.contains("nexus_game_info_destroy"),
            "missing destroy function"
        );
        assert!(c.contains("nexus_send_game_info"), "missing send function");
        assert!(c.contains("nexus_recv_game_info"), "missing recv function");

        // Struct name
        assert!(
            c.contains("NexusGameInfo"),
            "missing struct name in functions"
        );

        // Env var name
        assert!(c.contains("NEXUS_GAME_INFO_ADDR"), "missing env var");

        // Default URL
        assert!(
            c.contains("http://localhost:8080/nexus/game_info"),
            "missing default URL"
        );

        // curl API usage
        assert!(
            c.contains("curl_global_init(CURL_GLOBAL_DEFAULT)"),
            "missing curl_global_init"
        );
        assert!(
            c.contains("curl_easy_init()"),
            "missing curl_easy_init"
        );
        assert!(
            c.contains("curl_easy_cleanup"),
            "missing curl_easy_cleanup"
        );
        assert!(
            c.contains("curl_global_cleanup"),
            "missing curl_global_cleanup"
        );
        assert!(
            c.contains("CURLOPT_POSTFIELDS"),
            "missing CURLOPT_POSTFIELDS"
        );
        assert!(
            c.contains("CURLOPT_POSTFIELDSIZE"),
            "missing CURLOPT_POSTFIELDSIZE"
        );
        assert!(
            c.contains("Content-Type: application/octet-stream"),
            "missing Content-Type header"
        );
        assert!(
            c.contains("CURLOPT_WRITEFUNCTION"),
            "missing CURLOPT_WRITEFUNCTION"
        );
        assert!(
            c.contains("CURLOPT_WRITEDATA"),
            "missing CURLOPT_WRITEDATA"
        );
    }
}
