#[cfg(test)]
mod tests {
    use nexus_core::{Contract, Schema, SchemaIdx, StructDef, Transport};

    use super::*;

    fn test_contract() -> (Contract, Schema) {
        let contract = Contract {
            name: "game_info".to_string(),
            transport: Transport::Grpc,
            schema: SchemaIdx(0),
        };
        let schema = Schema {
            name: "game_info".to_string(),
            structs: vec![StructDef {
                name: "game_info".to_string(),
                fields: vec![],
            }],
        };
        (contract, schema)
    }

    #[test]
    fn test_impl_generation() {
        let (contract, schema) = test_contract();
        let file = generate_impl(&contract, &schema).unwrap();

        assert_eq!(file.path, "src/nexus_game_info.c");

        let c = &file.content;

        // Includes
        assert!(
            c.contains("#include \"nexus_game_info.h\""),
            "missing own header include"
        );
        assert!(
            c.contains("#include <grpc/grpc.h>"),
            "missing grpc include"
        );
        assert!(
            c.contains("#include <grpc/byte_buffer.h>"),
            "missing grpc byte_buffer include"
        );
        assert!(
            c.contains("#include <grpc/support/alloc.h>"),
            "missing grpc alloc include"
        );
        assert!(c.contains("#include <stdlib.h>"), "missing stdlib include");
        assert!(c.contains("#include <string.h>"), "missing string include");
        assert!(c.contains("#include <stdio.h>"), "missing stdio include");

        // Static channel
        assert!(
            c.contains("static grpc_channel* _nexus_game_info_channel = NULL;"),
            "missing static channel"
        );

        // Functions
        assert!(c.contains("nexus_game_info_init"), "missing init function");
        assert!(
            c.contains("nexus_game_info_destroy"),
            "missing destroy function"
        );
        assert!(c.contains("nexus_send_game_info"), "missing send function");
        assert!(c.contains("nexus_recv_game_info"), "missing recv function");

        // Struct name
        assert!(
            c.contains("NexusGameInfo"),
            "missing struct name in functions"
        );

        // Env var name
        assert!(c.contains("NEXUS_GAME_INFO_ADDR"), "missing env var");

        // Default gRPC address
        assert!(
            c.contains("localhost:50052"),
            "missing default grpc address"
        );

        // gRPC method paths
        assert!(
            c.contains("/nexus.GameInfo/Send"),
            "missing grpc Send method path"
        );
        assert!(
            c.contains("/nexus.GameInfo/Recv"),
            "missing grpc Recv method path"
        );
    }
}
